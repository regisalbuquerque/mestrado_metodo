package br.ufam.metodo.diversidade.util;

/**
 *
 * @author regis
 */
public class Diversidades {
    double ambiguidade;
    double margem;
    double qstatistics;
    double correlation;
    double disagreement;
    double doubleFault;
    
	public double getAmbiguidade() {
		return ambiguidade;
	}
	public void setAmbiguidade(double ambiguidade) {
		this.ambiguidade = ambiguidade;
	}
	public double getMargem() {
		return margem;
	}
	public void setMargem(double margem) {
		this.margem = margem;
	}
	public double getQstatistics() {
		return qstatistics;
	}
	public void setQstatistics(double qstatistics) {
		this.qstatistics = qstatistics;
	}
	public double getCorrelation() {
		return correlation;
	}
	public void setCorrelation(double correlation) {
		this.correlation = correlation;
	}
	public double getDisagreement() {
		return disagreement;
	}
	public void setDisagreement(double disagreement) {
		this.disagreement = disagreement;
	}
	public double getDoubleFault() {
		return doubleFault;
	}
	public void setDoubleFault(double doubleFault) {
		this.doubleFault = doubleFault;
	}
	

    

}
